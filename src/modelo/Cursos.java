package modelo;

public class Cursos {

    String id;
    String nome;
    String total_semestres;
    String total_horas;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTotal_semestres() {
        return total_semestres;
    }

    public void setTotal_semestres(String total_semestres) {
        this.total_semestres = total_semestres;
    }

    public String getTotal_horas() {
        return total_horas;
    }

    public void setTotal_horas(String total_horas) {
        this.total_horas = total_horas;
    }

}
