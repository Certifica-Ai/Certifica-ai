    private void btnbuscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscar1ActionPerformed
        ativaBotao2();
        try {
            Cursos cursos = new Cursos();
            cursos.setId(txtid1.getText());
            CursoDAO dao = new CursoDAO();
            dao.select(cursos);
            Connection connection = new ConnectionFactory().getConnection();
            String sql = "select * from tab_cursos where id = ? ";
            PreparedStatement stmt = connection.prepareStatement(sql);
            stmt.setString(1, txtid1.getText());
            ResultSet rs = stmt.executeQuery();

            if (txtid1.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Informe o Id!");
            } else if (rs.next()) {
                txtnome1.setText(cursos.getNome());
                txttsemestres.setText(cursos.getTotal_semestres());
                txtthoras.setText(cursos.getTotal_horas());
                ;
            } else {
                JOptionPane.showMessageDialog(null, "Curso não encontrado");
               // desativaBotao2();
                limpacampos2();

            }
            rs.close();
            connection.close();
        } catch (Exception e) {

        }    }//GEN-LAST:event_btnbuscar1ActionPerformed

