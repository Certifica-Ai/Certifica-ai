    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        ativaBotao1();
        try {
            Aluno aluno = new Aluno();
            aluno.setId(txtid.getText());
            AlunoDAO dao = new AlunoDAO();
            dao.select(aluno);
            Connection connection = new ConnectionFactory().getConnection();
            String sql = "select * from tab_alunos where id = ? ";
            PreparedStatement stmt = connection.prepareStatement(sql);
            stmt.setString(1, txtid.getText());
            ResultSet rs = stmt.executeQuery();

            if (txtid.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Informe o Id!");
            } else if (rs.next()) {
                txtra.setText(aluno.getRa());
                txtnome.setText(aluno.getNome());
                txtemail.setText(aluno.getEmail());
                txtcurso.setText(aluno.getCurso_aluno());
                txtsemestre.setText(aluno.getSemestre());
                txtsenha.setText(aluno.getSenha());
                ;
            } else {
                JOptionPane.showMessageDialog(null, "Usuario não encontrado");
                limpacampos1();
             //   desativaBotao1();

            }
            rs.close();
            connection.close();
        } catch (Exception e) {

        }    }//GEN-LAST:event_btnbuscarActionPerformed

